namespace QBMS
{
// ----------------------------------------------------------------------------
Quadric::Quadric()
{
	for (uint val = 0; val < MAX_VALUES; ++val)
		values_[val] = 0.f;
}
// ----------------------------------------------------------------------------
Quadric::Quadric(double parValues[])
{
	for (uint val = 0; val < MAX_VALUES; ++val)
		values_[val] = parValues[val];
}
// ----------------------------------------------------------------------------
Quadric::~Quadric()
{
}
// ----------------------------------------------------------------------------
void Quadric::Add(const Quadric& parQuadric)
{
	Add(parQuadric.Values());
}
// ----------------------------------------------------------------------------
void Quadric::Add(const double parValues[])
{
	for (uint val = 0; val < MAX_VALUES; ++val)
		values_[val] += parValues[val];
}
// ----------------------------------------------------------------------------
void Quadric::Init()
{
	for (uint val = 0; val < MAX_VALUES; ++val)
		values_[val] = 0.0;
}
// ----------------------------------------------------------------------------
}
