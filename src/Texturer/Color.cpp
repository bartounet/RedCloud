
#include "Color.h"


// ============================================================================
// ----------------------------------------------------------------------------
// ============================================================================
Color::Color (uchar parR, uchar parG, uchar parB)
    : r_(parR),
      g_(parG),
      b_(parB)
{
}
// ============================================================================
// ----------------------------------------------------------------------------
// ============================================================================
std::ostream& operator<<( std::ostream &flux, Color* const& c)
{
    flux << "(" << c->r_ << ", " << c->g_ << ", "  << c->b_ << ")";
    return flux;
}
