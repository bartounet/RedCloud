
#include "face.h"
#include "vertex.h"
#include <assert.h>


// ============================================================================
// ----------------------------------------------------------------------------
// ============================================================================
namespace QBMS
{
// ----------------------------------------------------------------------------
Face::Face() :
	v0_(0),
	v1_(0),
	v2_(0)
{
}
// ----------------------------------------------------------------------------
Face::Face(Vertex* parV0, Vertex* parV1, Vertex* parV2) :
	v0_(parV0),
	v1_(parV1),
	v2_(parV2)
{
	assert(v0_);
	assert(v1_);
	assert(v2_);
}
// ----------------------------------------------------------------------------
Face::~Face()
{
}
// ----------------------------------------------------------------------------
void Face::RemoveOnRelatedVertex()
{
	assert(IsDegenerated());

	if (v0_ == v1_)
	{
		if (v0_ != v2_)
			v2_->RemoveIncidentFace(this);
	}
	else
		v1_->RemoveIncidentFace(this);

	v0_->RemoveIncidentFace(this);
}
// ----------------------------------------------------------------------------
}
