#ifndef GEOMETRY_H_
#define GEOMETRY_H_


#include "types.h"
#include <vector>


// ============================================================================
// ----------------------------------------------------------------------------
// ============================================================================
struct Vertex
{
	float x, y, z; // position
	float nx, ny, nz; // normal
	uchar r, g, b; // color
};
// ----------------------------------------------------------------------------
struct Face
{
	uint v0, v1, v2; // vertex indices
};
// ----------------------------------------------------------------------------
struct Mesh
{
	std::vector<Vertex> vertices;
	std::vector<Face> faces;
};
// ============================================================================
// ----------------------------------------------------------------------------
// ============================================================================


#endif