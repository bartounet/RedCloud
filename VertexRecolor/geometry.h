#ifndef GEOMETRY_H_
#define GEOMETRY_H_


#include "types.h"
#include <vector>


// ============================================================================
// ----------------------------------------------------------------------------
// ============================================================================

struct Vertex
{
	float x, y, z; // position
	float nx, ny, nz; // normal
	float r, g, b; // color
};

// ----------------------------------------------------------------------------

struct Face
{
	uint v0, v1, v2; // vertex indices
};

// ----------------------------------------------------------------------------

struct Mesh
{
	uint nbVertices;
	std::vector<Vertex> vertices;

	uint32 nbFaces;
	std::vector<Face> faces;
};

// ============================================================================
// ----------------------------------------------------------------------------
// ============================================================================


#endif